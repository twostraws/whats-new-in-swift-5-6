## Plugins for Swift Package Manager

Swift 5.6 includes a raft ([[1](https://github.com/apple/swift-evolution/blob/main/proposals/0303-swiftpm-extensible-build-tools.md)], [[2](https://github.com/apple/swift-evolution/blob/main/proposals/0305-swiftpm-binary-target-improvements.md)], [[3](https://github.com/apple/swift-evolution/blob/main/proposals/0325-swiftpm-additional-plugin-apis.md)], [[4](https://github.com/apple/swift-evolution/blob/main/proposals/0332-swiftpm-command-plugins.md)]) of improvements to Swift Package Manager, which combine to add the beginnings of plugin support using external build tools.

At this point earlier adopters are [reporting](https://twitter.com/tonyarnold/status/1498508218934644737) that the functionality isn’t quite far along enough to support more [complex use cases](https://twitter.com/merowing_/status/1498567302702374912), but it does at least seem to support SwiftGen and there are further examples demonstrating possibilities for DocC and swift-format.

